func scanVersion(row interface{ Scan(dest ...any) error }) (domaincashier.TemplateVersionRecord, error) {
	var item domaincashier.TemplateVersionRecord
	var sourceType string
	var status string
	var versionString string
	err := row.Scan(
		&item.ID, &item.TemplateIDRef, &versionString, &sourceType, &item.AutoGenerated, &item.FXBaseDate,
		&status, &item.Checksum, &item.PublishedAt, &item.CreatedAt, &item.UpdatedAt,
	)
	if err != nil {
		return domaincashier.TemplateVersionRecord{}, mapErr(err)
	}
	v, convErr := strconv.Atoi(versionString)
	if convErr != nil {
		return domaincashier.TemplateVersionRecord{}, convErr
	}
	item.Version = v
	item.SourceType = domaincashier.SourceType(sourceType)
	item.Status = domaincashier.VersionStatus(status)
	return item, nil
}

func (r *CashierRepo) GetPublishedVersion(ctx context.Context, templateIDRef int64) (*domaincashier.TemplateVersionRecord, error) {
	row := r.db.QueryRow(ctx, `
SELECT id, template_id_ref, version, source_type, auto_generated, fx_base_date, status, checksum, published_at, created_at, updated_at
FROM platform.cashier_price_template_versions
WHERE template_id_ref=$1 AND status='published'
ORDER BY published_at DESC NULLS LAST, id DESC
LIMIT 1`, templateIDRef)
	item, err := scanVersion(row)
	if err != nil {
		if err == adminapp.ErrNotFound {
			return nil, nil
		}
		return nil, err
	}
	return &item, nil
}

func (r *CashierRepo) NextVersion(ctx context.Context, templateIDRef int64) (int, error) {
	var max int
	if err := r.db.QueryRow(ctx, `
SELECT COALESCE(MAX(CAST(version AS INTEGER)), 0)
FROM platform.cashier_price_template_versions
WHERE template_id_ref=$1`, templateIDRef).Scan(&max); err != nil {
		return 0, mapErr(err)
	}
	return max + 1, nil
}

