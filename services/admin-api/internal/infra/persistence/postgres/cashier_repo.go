func (r *CashierRepo) CreateFXSyncRun(ctx context.Context, run domaincashier.FXSyncRun) (domaincashier.FXSyncRun, error) {
	diffRaw, err := json.Marshal(run.DiffSummaryJSON)
	if err != nil {
		return domaincashier.FXSyncRun{}, err
	}
	var out domaincashier.FXSyncRun
	var status string
	var raw []byte
	err = r.db.QueryRow(ctx, `
INSERT INTO platform.cashier_fx_sync_runs
  (template_id_ref, candidate_version_id_ref, status, diff_summary_json, review_note)
VALUES ($1,$2,$3,$4::jsonb,$5)
RETURNING id, template_id_ref, candidate_version_id_ref, status, diff_summary_json, triggered_at, reviewed_by, reviewed_at, review_note, created_at, updated_at`,
		run.TemplateIDRef, run.CandidateVersionIDRef, string(run.Status), string(diffRaw), run.ReviewNote,
	).Scan(
		&out.ID, &out.TemplateIDRef, &out.CandidateVersionIDRef, &status, &raw, &out.TriggeredAt,
		&out.ReviewedBy, &out.ReviewedAt, &out.ReviewNote, &out.CreatedAt, &out.UpdatedAt,
	)
	if err != nil {
		return domaincashier.FXSyncRun{}, mapErr(err)
	}
	_ = json.Unmarshal(raw, &out.DiffSummaryJSON)
	out.Status = domaincashier.FXRunStatus(status)
	return out, nil
}

