func (r *CashierRepo) ListTemplates(ctx context.Context, page, pageSize int) ([]domaincashier.PriceTemplate, int, error) {
	offset := (page - 1) * pageSize
	rows, err := r.db.Query(ctx, `
SELECT id, template_id, template_name, fx_sync_enabled, fx_sync_mode, fx_sync_schedule, status
FROM platform.cashier_price_templates
ORDER BY updated_at DESC, id DESC
LIMIT $1 OFFSET $2`, pageSize, offset)
	if err != nil {
		return nil, 0, mapErr(err)
	}
	defer rows.Close()
	items := make([]domaincashier.PriceTemplate, 0)
	for rows.Next() {
		var it domaincashier.PriceTemplate
		if err := rows.Scan(&it.ID, &it.TemplateID, &it.TemplateName, &it.FXSyncEnabled, &it.FXSyncMode, &it.FXSyncSchedule, &it.Status); err != nil {
			return nil, 0, mapErr(err)
		}
		items = append(items, it)
	}
	var total int
	if err := r.db.QueryRow(ctx, `SELECT COUNT(*) FROM platform.cashier_price_templates`).Scan(&total); err != nil {
		return nil, 0, mapErr(err)
	}
	return items, total, mapErr(rows.Err())
}

