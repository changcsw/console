package cashier

import (
	"encoding/json"
	"errors"
	"io"
	"net/http"
	"strconv"
	"strings"
	"time"

	cashierapp "github.com/csw/console/services/admin-api/internal/app/cashier"
	domaincashier "github.com/csw/console/services/admin-api/internal/domain/cashier"
	"github.com/csw/console/services/admin-api/internal/transport/http/httpx"
	"github.com/go-chi/chi/v5"
)

type Handler struct {
	svc *cashierapp.Service
}

func NewHandler(svc *cashierapp.Service) *Handler {
	return &Handler{svc: svc}
}

type createTemplateRequest struct {
	TemplateID     string `json:"templateId"`
	TemplateName   string `json:"templateName"`
	FXSyncEnabled  bool   `json:"fxSyncEnabled"`
	FXSyncMode     string `json:"fxSyncMode"`
	FXSyncSchedule string `json:"fxSyncSchedule"`
}

type createVersionRequest struct {
	SourceType    string `json:"sourceType"`
	SourceVersion string `json:"sourceVersion"`
}

type upsertRowsRequest struct {
	Rows []struct {
		CountryCode  string `json:"countryCode"`
		RegionCode   string `json:"regionCode"`
		Currency     string `json:"currency"`
		PriceID      string `json:"priceId"`
		PreTaxAmount string `json:"preTaxAmount"`
		TaxRate      string `json:"taxRate"`
		EffectiveAt  string `json:"effectiveAt"`
	} `json:"rows"`
}

type approveFXRunRequest struct {
	ReviewNote string `json:"reviewNote"`
	Action     string `json:"action"`
}

type bindGameProfileRequest struct {
	TemplateID      string `json:"templateId"`
	TemplateVersion string `json:"templateVersion"`
}

type savePriceOverridesRequest struct {
	Items []struct {
		CountryCode         string `json:"countryCode"`
		RegionCode          string `json:"regionCode"`
		Currency            string `json:"currency"`
		PriceID             string `json:"priceId"`
		PreTaxAmountMinor   int64  `json:"preTaxAmountMinor"`
		TaxRate             string `json:"taxRate"`
		TaxAmountMinor      int64  `json:"taxAmountMinor"`
		AfterTaxAmountMinor int64  `json:"afterTaxAmountMinor"`
		Reason              string `json:"reason"`
		EffectiveAt         string `json:"effectiveAt"`
	} `json:"items"`
}

type versionResponse struct {
	Version       int     `json:"version"`
	Status        string  `json:"status"`
	SourceType    string  `json:"sourceType,omitempty"`
	AutoGenerated bool    `json:"autoGenerated,omitempty"`
	PublishedAt   *string `json:"publishedAt,omitempty"`
	CreatedAt     string  `json:"createdAt,omitempty"`
}

type fxRunResponse struct {
	RunID            int64          `json:"runId"`
	CandidateVersion string         `json:"candidateVersion"`
	Status           string         `json:"status"`
	DiffSummary      map[string]any `json:"diffSummary"`
	TriggeredAt      string         `json:"triggeredAt"`
	ReviewedBy       *int64         `json:"reviewedBy,omitempty"`
	ReviewedAt       *string        `json:"reviewedAt,omitempty"`
	ReviewNote       string         `json:"reviewNote"`
}

type templateResponse struct {
	TemplateID     string `json:"templateId"`
	TemplateName   string `json:"templateName"`
	FXSyncEnabled  bool   `json:"fxSyncEnabled"`
	FXSyncMode     string `json:"fxSyncMode"`
	FXSyncSchedule string `json:"fxSyncSchedule"`
	Status         string `json:"status"`
}

type gameProfileResponse struct {
	TemplateID             string `json:"templateId"`
	AppliedTemplateVersion string `json:"appliedTemplateVersion"`
	SnapshotChecksum       string `json:"snapshotChecksum"`
	AppliedAt              string `json:"appliedAt"`
}

type gamePriceOverrideResponse struct {
	CountryCode         string `json:"countryCode"`
	RegionCode          string `json:"regionCode"`
	Currency            string `json:"currency"`
	PriceID             string `json:"priceId"`
	PreTaxAmountMinor   int64  `json:"preTaxAmountMinor"`
	TaxRate             string `json:"taxRate"`
	TaxAmountMinor      int64  `json:"taxAmountMinor"`
	AfterTaxAmountMinor int64  `json:"afterTaxAmountMinor"`
	Reason              string `json:"reason"`
	EffectiveAt         string `json:"effectiveAt"`
}

func toTemplateResponse(t domaincashier.PriceTemplate) templateResponse {
	return templateResponse{
		TemplateID:     t.TemplateID,
		TemplateName:   t.TemplateName,
		FXSyncEnabled:  t.FXSyncEnabled,
		FXSyncMode:     t.FXSyncMode,
		FXSyncSchedule: t.FXSyncSchedule,
		Status:         t.Status,
	}
}

func toGameProfileResponse(v cashierapp.GameCashierProfileView) gameProfileResponse {
	return gameProfileResponse{
		TemplateID:             v.TemplateID,
		AppliedTemplateVersion: strconv.Itoa(v.AppliedTemplateVersion),
		SnapshotChecksum:       v.SnapshotChecksum,
		AppliedAt:              v.AppliedAt.UTC().Format(time.RFC3339),
	}
}

func toGamePriceOverrideResponse(v domaincashier.GameCashierPriceOverride) gamePriceOverrideResponse {
	return gamePriceOverrideResponse{
		CountryCode:         v.CountryCode,
		RegionCode:          v.RegionCode,
		Currency:            v.Currency,
		PriceID:             v.PriceID,
		PreTaxAmountMinor:   v.PreTaxAmountMinor,
		TaxRate:             v.TaxRate,
		TaxAmountMinor:      v.TaxAmountMinor,
		AfterTaxAmountMinor: v.AfterTaxAmountMinor,
		Reason:              v.Reason,
		EffectiveAt:         v.EffectiveAt.UTC().Format(time.RFC3339),
	}
}

func toVersionResponse(v domaincashier.TemplateVersionRecord) versionResponse {
	out := versionResponse{
		Version:       v.Version,
		Status:        string(v.Status),
		SourceType:    string(v.SourceType),
		AutoGenerated: v.AutoGenerated,
		CreatedAt:     v.CreatedAt.UTC().Format(time.RFC3339),
	}
	if v.PublishedAt != nil {
		s := v.PublishedAt.UTC().Format(time.RFC3339)
		out.PublishedAt = &s
	}
	return out
}

func toFXRunResponse(v cashierapp.FXSyncRunView) fxRunResponse {
	r := v.Run
	out := fxRunResponse{
		RunID:            r.ID,
		CandidateVersion: strconv.Itoa(v.CandidateVersion),
		Status:           string(r.Status),
		DiffSummary:      r.DiffSummaryJSON,
		TriggeredAt:      r.TriggeredAt.UTC().Format(time.RFC3339),
		ReviewedBy:       r.ReviewedBy,
		ReviewNote:       r.ReviewNote,
	}
	if r.ReviewedAt != nil {
		s := r.ReviewedAt.UTC().Format(time.RFC3339)
		out.ReviewedAt = &s
	}
	return out
}

func mapFXRuns(items []cashierapp.FXSyncRunView) []fxRunResponse {
	out := make([]fxRunResponse, 0, len(items))
	for _, item := range items {
		out = append(out, toFXRunResponse(item))
	}
	return out
}

func (h *Handler) ListTemplates(w http.ResponseWriter, r *http.Request) {
	page, pageSize := parsePage(r)
	items, total, err := h.svc.ListTemplates(r.Context(), page, pageSize)
	if err != nil {
		writeError(w, err)
		return
	}
	httpx.WriteData(w, http.StatusOK, map[string]any{
		"items":    items,
		"page":     page,
		"pageSize": pageSize,
		"total":    total,
	})
}

func (h *Handler) CreateTemplate(w http.ResponseWriter, r *http.Request) {
	var req createTemplateRequest
	if err := decodeJSON(r, &req); err != nil {
		httpx.WriteError(w, http.StatusBadRequest, httpx.CodeValidation, "请求体格式错误")
		return
	}
	if req.FXSyncMode == "" {
		req.FXSyncMode = "manual_confirm"
	}
	if req.FXSyncSchedule == "" {
		req.FXSyncSchedule = "monthly"
	}
	created, err := h.svc.CreateTemplate(r.Context(), cashierapp.CreateTemplateInput{
		TemplateID:     req.TemplateID,
		TemplateName:   req.TemplateName,
		FXSyncEnabled:  req.FXSyncEnabled,
		FXSyncMode:     req.FXSyncMode,
		FXSyncSchedule: req.FXSyncSchedule,
	})
	if err != nil {
		writeError(w, err)
		return
	}
	httpx.WriteData(w, http.StatusCreated, created)
}

func (h *Handler) GetTemplate(w http.ResponseWriter, r *http.Request) {
	templateID := chi.URLParam(r, "templateId")
	tpl, versions, err := h.svc.GetTemplate(r.Context(), templateID)
	if err != nil {
		writeError(w, err)
		return
	}
	runs, err := h.svc.ListFXSyncRuns(r.Context(), templateID)
	if err != nil {
		writeError(w, err)
		return
	}
	httpx.WriteData(w, http.StatusOK, map[string]any{
		"template":   toTemplateResponse(tpl),
		"versions":   mapVersions(versions),
		"fxSyncRuns": mapFXRuns(runs),
	})
}

func (h *Handler) CreateVersion(w http.ResponseWriter, r *http.Request) {
	var req createVersionRequest
	if err := decodeJSON(r, &req); err != nil {
		httpx.WriteError(w, http.StatusBadRequest, httpx.CodeValidation, "请求体格式错误")
		return
	}
	sourceType := domaincashier.SourceType(req.SourceType)
	if sourceType == "" {
		sourceType = domaincashier.SourceTypeManual
	}
	sourceVersion := 0
	if raw := strings.TrimSpace(req.SourceVersion); raw != "" {
		v, convErr := strconv.Atoi(raw)
		if convErr != nil || v <= 0 {
			httpx.WriteError(w, http.StatusBadRequest, httpx.CodeValidation, "sourceVersion 非法")
			return
		}
		sourceVersion = v
	}
	record, err := h.svc.CreateVersion(r.Context(), cashierapp.CreateVersionInput{
		TemplateID:    chi.URLParam(r, "templateId"),
		SourceType:    sourceType,
		SourceVersion: sourceVersion,
	})
	if err != nil {
		writeError(w, err)
		return
	}
	httpx.WriteData(w, http.StatusCreated, toVersionResponse(record))
}

