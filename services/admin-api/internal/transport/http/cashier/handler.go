func (h *Handler) UpsertRows(w http.ResponseWriter, r *http.Request) {
	version, ok := parseVersion(w, r)
	if !ok {
		return
	}
	var req upsertRowsRequest
	if err := decodeJSON(r, &req); err != nil {
		httpx.WriteError(w, http.StatusBadRequest, httpx.CodeValidation, "请求体格式错误")
		return
	}
	rows := make([]cashierapp.UpsertPriceRowInput, 0, len(req.Rows))
	for _, item := range req.Rows {
		effectiveAt, err := time.Parse(time.RFC3339, item.EffectiveAt)
		if err != nil {
			httpx.WriteError(w, http.StatusBadRequest, httpx.CodeValidation, "effectiveAt 非法")
			return
		}
		rows = append(rows, cashierapp.UpsertPriceRowInput{
			CountryCode:  item.CountryCode,
			RegionCode:   item.RegionCode,
			Currency:     item.Currency,
			PriceID:      item.PriceID,
			PreTaxAmount: item.PreTaxAmount,
			TaxRate:      item.TaxRate,
			EffectiveAt:  effectiveAt,
		})
	}
	out, err := h.svc.UpsertRows(r.Context(), chi.URLParam(r, "templateId"), version, rows)
	if err != nil {
		writeError(w, err)
		return
	}
	httpx.WriteData(w, http.StatusOK, map[string]any{"items": out})
}

func (h *Handler) PublishVersion(w http.ResponseWriter, r *http.Request) {
	version, ok := parseVersion(w, r)
	if !ok {
		return
	}
	if err := h.svc.PublishVersion(r.Context(), chi.URLParam(r, "templateId"), version); err != nil {
		writeError(w, err)
		return
	}
	httpx.WriteData(w, http.StatusOK, map[string]any{"status": "published"})
}

